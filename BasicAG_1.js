// 재귀 함수
// 순번     f(n)     n     return      최종
//                         n * f(n - 1)
//  1      f(5)     5      5*f(4)    5 * 24
//  2      f(4)     4      4*f(3)    4 * 6
//  3      f(3)     3      3*f(2)    3 * 2
//  4      f(2)     2      2*f(1)    2 * 1
//  5      f(1)     1      1

function f(n) {
  if (n <= 1) {
    return 1;
  }
  return n * f(n - 1);
}
console.log(`재귀 함수 : ${f(5)}`); // 재귀 함수 : 5050;
